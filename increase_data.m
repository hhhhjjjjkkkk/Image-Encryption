function [G1,M1]=increase_data(p,a1,a2)
u=0.77;%初始密钥
[M,N]=size(p);
n0=floor(M*N*u);
n1=n0-mod(n0,3);%n1是添加数组的长度
%--------------------------生成随机数--------------------------------------
for i=1:n1 %整数部分的生成
    s2(i)=mod(floor(double(a1(i))*1200),1200);%这个值越大扩散越好2000
    s3(i)=mod(floor(double(a2(i))*10000),10000);%这个值越大扩散越好2000
    s4=s2+0.0001*s3;%生成伪随机序列
end
s5=reshape(s4,n1/3,3);
G1=[p;s5];
M1=n1/3+M;%总体行数
end